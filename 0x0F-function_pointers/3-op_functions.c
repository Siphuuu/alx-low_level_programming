#include "3-calc.h"

/**
 * op_add - calculate integer
 * @a: first integer
 * @b: second integer
 * Return: the sum
 */
int op_add(int a, int b)
{
	return (a + b);
}

/**
 * op_sub - substraction
 * @a: first number
 * @b: second number
 * Return: the diffrence
 */

int op_sub(int a, int b)
{
	return (a - b);
}

/**
 * op_mul - multiplication
 * @a: first number
 * @b: second number
 * Return: multiplied value
 */

int op_mul(int a, int b)
{
	return (a * b);
}

/**
 * op_div - division
 * @a:first number
 * @b: second number
 * Return: the dided value
 */
int op_div(int a, int b)
{
	return (a / b);
}

/**
 * op_mod - remainder of the division
 * @a: first number
 * @b: second number
 * Return: remainder
 */

int op_mod(int a, int b)
{
	return (a % b);
}
